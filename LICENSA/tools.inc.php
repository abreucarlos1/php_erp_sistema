//Retorna os anos, meses e dias de diferença entre datas
//array
function offset_data($startDate, $endDate) 
{
	//data (YYYY-MM-AA)
	$datai = explode("-",$startDate);
	$dataf = explode("-",$endDate);
	 
	$startDate = mktime(0,0,0,$datai[1], $datai[2], $datai[0]); 
	$endDate = mktime(0,0,0,$dataf[1], $dataf[2], $dataf[0]); 
	
	if ($startDate === false || $startDate < 0 || $endDate === false || $endDate < 0 || $startDate > $endDate) 
		return false; 
		
	$years = date('Y', $endDate) - date('Y', $startDate); 
	
	$endMonth = date('m', $endDate); 
	$startMonth = date('m', $startDate);
	
	$endDays = date('d', $endDate); 
	$startDays = date('d', $startDate);  
	
	// Calculate months 
	$months = $endMonth - $startMonth; 
	
	if ($months <= 0)  
	{ 
		$months += 12; 
		$years--; 
	} 
	
	if ($years < 0) 
		return false; 
	
	// Calculate the days 

	$days = $endDays-$startDays;
	
	if($days<0)
	{
		$months--;
		
		switch ($endMonth-1)
		{
			//meses com 31 dias
			case 1:
			case 3:
			case 5:
			case 7:
			case 8:
			case 10:
			case 12:
				$days = 31 - abs($days);
			break;
			
			case 2:
				$days = 28 - abs($days);
			break;
			
			case 4:
			case 6:
			case 9:
			case 11:
				$days = 30 - abs($days);
			break;	
		}
	}	
					
	return array($years, $months, $days); 
} 

function valorPorExtenso($valor=0) 
{
	//RETORNA O VALOR EXTENSO DE $valor
	$singular = array("centavo", "real", "mil", "milhão", "bilhão", "trilhão", "quatrilhão");
	$plural = array("centavos", "reais", "mil", "milhões", "bilhões", "trilhões","quatrilhões");

	$c = array("", "cem", "duzentos", "trezentos", "quatrocentos","quinhentos", "seiscentos", "setecentos", "oitocentos", "novecentos");
	$d = array("", "dez", "vinte", "trinta", "quarenta", "cinquenta","sessenta", "setenta", "oitenta", "noventa");
	$d10 = array("dez", "onze", "doze", "treze", "quatorze", "quinze","dezesseis", "dezesete", "dezoito", "dezenove");
	$u = array("", "um", "dois", "três", "quatro", "cinco", "seis","sete", "oito", "nove");

	$z=0;

	$valor = number_format($valor, 2, ".", ".");
	$inteiro = explode(".", $valor);
	for($i=0;$i<count($inteiro);$i++)
		for($ii=strlen($inteiro[$i]);$ii<3;$ii++)
			$inteiro[$i] = "0".$inteiro[$i];

	// $fim identifica onde que deve se dar jun��o de centenas por "e" ou por "," ;)
	$fim = count($inteiro) - ($inteiro[count($inteiro)-1] > 0 ? 1 : 2);
	
	for ($i=0;$i<count($inteiro);$i++) 
	{
		$valor = $inteiro[$i];
		$rc = (($valor > 100) && ($valor < 200)) ? "cento" : $c[$valor[0]];
		$rd = ($valor[1] < 2) ? "" : $d[$valor[1]];
		$ru = ($valor > 0) ? (($valor[1] == 1) ? $d10[$valor[2]] : $u[$valor[2]]) : "";
	
		$r = $rc.(($rc && ($rd || $ru)) ? " e " : "").$rd.(($rd && $ru) ? " e " : "").$ru;
		$t = count($inteiro)-1-$i;
		$r .= $r ? " ".($valor > 1 ? $plural[$t] : $singular[$t]) : "";
		if ($valor == "000")
			$z++;
		elseif ($z > 0) 
			$z--;
		if (($t==1) && ($z>0) && ($inteiro[0] > 0)) 
			$r .= (($z>1) ? " de " : "").$plural[$t]; 
		if ($r) 
			$rt = $rt . ((($i > 0) && ($i <= $fim) && ($inteiro[0] > 0) && ($z < 1)) ? ( ($i < $fim) ? ", " : " e ") : " ") . $r;
	}

	return($rt ? $rt : "zero");
}

