	$tmp2 = array_pop($array_not_2);
	
	//Verifica os elementos retornados, não podem ser vazios
	if(!is_null($tmp1) && !is_null($tmp2))
	{
		$horas[2] += abs(time_to_sec($tmp1)-time_to_sec($tmp2));
	}

	return $horas;	
}

