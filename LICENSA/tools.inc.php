					$rev[$i-1] = chr(ord($rev[$i-1])+1);
				}
				else
				{
					$rev[$i-1] = $rev[$i-1] + 1;
				}
				$rev[$i] = chr(ord($rev[$i])-25);
			}
			else
			{
				$rev[$i] = chr(ord($rev[$i])+1);
			}
			break;
		}
		else
		{
			if($rev[$i]==9)
			{
				if(preg_match("([A-Z,a-z])",$rev[$i-1]))
				{
					$rev[$i-1] = chr(ord($rev[$i-1])+1);
