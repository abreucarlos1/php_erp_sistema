function versao_documento($versao_documento)
{
	// Incrementa revisão automaticamente (alphanum)
	$rev = $versao_documento;
	$len = strlen($rev);
	
	for($i=$len-1;$i>=0;$i--)
	{
		if(preg_match("([A-Z,a-z])",$rev[$i]))
		{
			//echo "sim";
			if($rev[$i]=='z' || $rev[$i]=='Z')
			{
